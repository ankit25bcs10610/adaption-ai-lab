#!/usr/bin/env node
/**
 * Wire real eval numbers into the landing page.
 *
 * Reads ../results/baseline.json and ../results/eval.json (produced by src/baseline.py and
 * src/eval_harness.py), then rewrites web/lib/results.ts with the real base-vs-fine-tuned values and
 * flips `representative` to false. Run from the web/ dir:  npm run sync:results
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const resultsDir = join(__dirname, "..", "..", "results");
const basePath = join(resultsDir, "baseline.json");
const ftPath = join(resultsDir, "eval.json");

if (!existsSync(basePath) || !existsSync(ftPath)) {
  console.error(
    `[sync] missing ${basePath} or ${ftPath}.\n` +
      `Run the pipeline first: python -m src.baseline && python -m src.eval_harness ...`
  );
  process.exit(1);
}

const base = JSON.parse(readFileSync(basePath, "utf8"));
const ft = JSON.parse(readFileSync(ftPath, "utf8"));
const pct = (x) => Math.round((x ?? 0) * 100);

const rows = [
  { label: "Overall accuracy", base: pct(base.overall_accuracy), ft: pct(ft.overall_accuracy) },
  { label: "Positive (tool-call) accuracy", base: pct(base.positive_accuracy), ft: pct(ft.positive_accuracy) },
  { label: "Refusal accuracy", base: pct(base.refusal_accuracy), ft: pct(ft.refusal_accuracy) },
  { label: "Clarify accuracy", base: pct(base.clarify_accuracy), ft: pct(ft.clarify_accuracy) },
  { label: "Hallucination rate", base: pct(base.hallucination_rate), ft: pct(ft.hallucination_rate), lowerIsBetter: true },
];

const overallDelta = pct(ft.overall_accuracy) - pct(base.overall_accuracy);
const halluDelta = pct(ft.hallucination_rate) - pct(base.hallucination_rate);

const out = `/**
 * Benchmark data for the landing page — AUTO-GENERATED by scripts/sync-results.mjs. Do not edit by hand.
 */
export interface BenchRow {
  label: string;
  base: number;
  ft: number;
  lowerIsBetter?: boolean;
}

export const representative = false;

export const headlineStats = [
  { value: "${halluDelta >= 0 ? "+" : "−"}${Math.abs(halluDelta)}pp", label: "hallucination rate", accent: "run" as const },
  { value: "${overallDelta >= 0 ? "+" : "−"}${Math.abs(overallDelta)}pp", label: "overall accuracy", accent: "cyan" as const },
  { value: "3B", label: "params · QLoRA", accent: "fg" as const },
];

export const benchmarks: BenchRow[] = ${JSON.stringify(rows, null, 2)};
`;

writeFileSync(join(__dirname, "..", "lib", "results.ts"), out);
console.log("[sync] wrote lib/results.ts with real eval numbers.");
