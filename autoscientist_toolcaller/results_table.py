"""Generate RESULTS.md — a one-glance base-vs-fine-tuned leaderboard, auto-filled from the eval JSONs.

Reads results/baseline.json + results/eval.json (+ optional eval_bfcl.json, eval_novel.json, HEADLINE.txt)
and writes a compact leaderboard with a Δ column. Degrades every cell to `__PENDING__` before the run, so
RESULTS.md is safe to commit today; `scripts/finalize.sh` regenerates it with real numbers on run-day.
RESULTS.md is NOT a release card, so `__PENDING__` here never trips `release preflight`/`lint-cards`.

Usage:
  python -m autoscientist_toolcaller.results_table --results-dir results --out RESULTS.md
"""
from __future__ import annotations

import argparse
import json
import os
from typing import Any, Dict, Optional

_PENDING = "__PENDING__"


def _load(path: str) -> Dict[str, Any]:
    return json.load(open(path)) if os.path.exists(path) else {}


def _cell(v: Optional[float]) -> str:
    return _PENDING if not isinstance(v, (int, float)) else f"{v:.3f}"


def _delta(b: Optional[float], f: Optional[float]) -> str:
    if not isinstance(b, (int, float)) or not isinstance(f, (int, float)):
        return _PENDING
    return f"{f - b:+.3f}"


def _row(label: str, base: Dict[str, Any], ft: Dict[str, Any], key: str) -> str:
    b, f = base.get(key), ft.get(key)
    return f"| {label} | {_cell(b)} | {_cell(f)} | {_delta(b, f)} |"


def render(base: Dict[str, Any], ft: Dict[str, Any], headline: str,
           bfcl_base: Dict[str, Any], bfcl_ft: Dict[str, Any], novel: Dict[str, Any]) -> str:
    lines = [
        "# Results — base vs. fine-tuned (function-calling)",
        "",
        "> Auto-generated by `python -m autoscientist_toolcaller.results_table` from `results/*.json`.",
        "> Cells stay unfilled until the AutoScientist console run produces weights; then",
        "> `bash scripts/finalize.sh` fills every number (no hand-transcription).",
        "",
        f"**Headline:** {headline or _PENDING}",
        "",
        "| Metric | Base | Fine-tuned | Δ |",
        "|---|---|---|---|",
        _row("Overall accuracy", base, ft, "overall_accuracy"),
        _row("Positive (tool-call) accuracy", base, ft, "positive_accuracy"),
        _row("Refusal accuracy", base, ft, "refusal_accuracy"),
        _row("Clarify accuracy", base, ft, "clarify_accuracy"),
        _row("**Hallucination rate on hard negatives** ↓", base, ft, "hallucination_rate"),
    ]
    # Weighted BFCL-v4 accuracy. A side whose eval simply wasn't run renders "—" (not __PENDING__):
    # only cells the pipeline is EXPECTED to fill may claim pending (finalize.sh writes both sides).
    wb, wf = bfcl_base.get("weighted_accuracy"), bfcl_ft.get("weighted_accuracy")
    if wb is not None or wf is not None:
        cb = f"{wb:.3f}" if isinstance(wb, (int, float)) else "—"
        cf = f"{wf:.3f}" if isinstance(wf, (int, float)) else "—"
        d = _delta(wb, wf) if (isinstance(wb, (int, float)) and isinstance(wf, (int, float))) else "—"
        lines.append(f"| Weighted BFCL-v4 accuracy | {cb} | {cf} | {d} |")
    # Format-invariance spread (max−min accuracy across tool-doc renderings) — lower is better.
    fd = bfcl_ft.get("format_delta")
    if isinstance(fd, (int, float)):
        lines.append(f"| Format-invariance Δ across doc formats ↓ | — | {fd:.3f} | — |")
    # Novel-tools holdout (generalization) — fine-tuned only unless a base novel eval exists.
    if novel:
        lines.append(f"| Novel-tools holdout accuracy | — | {_cell(novel.get('overall_accuracy'))} | — |")
    lines += [
        "",
        "**Eligibility gate:** the challenge scores a measurable % improvement over a baseline on Adaption's",
        "held-out in-house test set — the Δ column above, produced by the console run.",
        "",
        "**Separately measured today (a *data* metric, not model accuracy):** Adaptive Data graded the",
        "dataset quality **+15.7% (C→B)** — see `docs/DATA_QUALITY_AUDIT.md`.",
    ]
    return "\n".join(lines) + "\n"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--results-dir", default="results")
    ap.add_argument("--out", default="RESULTS.md")
    args = ap.parse_args()
    r = args.results_dir
    hl = ""
    if os.path.exists(os.path.join(r, "HEADLINE.txt")):
        hl = open(os.path.join(r, "HEADLINE.txt")).read().strip().splitlines()[0] if \
             open(os.path.join(r, "HEADLINE.txt")).read().strip() else ""
    text = render(_load(os.path.join(r, "baseline.json")), _load(os.path.join(r, "eval.json")), hl,
                  _load(os.path.join(r, "baseline_bfcl.json")), _load(os.path.join(r, "eval_bfcl.json")),
                  _load(os.path.join(r, "eval_novel.json")))
    open(args.out, "w", encoding="utf-8").write(text)
    n = text.count(_PENDING)
    print(f"[results_table] wrote {args.out} " + ("(all filled)" if n == 0 else f"({n} __PENDING__ — run finalize.sh)"))


if __name__ == "__main__":
    main()
